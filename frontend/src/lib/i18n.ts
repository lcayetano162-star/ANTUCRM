// ─── Translation function ────────────────────────────────────────────────────

/**
 * Returns the translation for `text` in the given language.
 * Falls back to the original string if no translation is found.
 * Supports simple {{variable}} interpolation.
 */
export function translate(text: string, lang: Lang, vars?: Record<string, string | number>): string {
  let result = lang === 'en' ? (EN[text] ?? text) : text
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      result = result.replace(new RegExp(`{{${k}}}`, 'g'), String(v))
    })
  }
  return result
}
